export class GarageDoor {

	constructor(){
	}

	up() : void {
		console.log('ガレージのドアオープン');
	}

	down() : void {
		console.log('ガレージのドアを閉める');
	}

	stop() : void {
		console.log('ガレージのドアをストップ');
	}

	lightOn() : void {
		console.log('ガレージの光をつける');
	}

	lightOff() : void {
		console.log('ガレージの光を消す');
	}
}
